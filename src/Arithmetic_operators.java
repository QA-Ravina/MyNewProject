public class Arithmetic_operators {
    public static void main(String[] args) {
        int A = 8;
        int B = 5;

        System.out.println(A+B);
        System.out.println(A-B);
        System.out.println(A*B);
        System.out.println(A/B);

        double X = 5;
        double Y = 3;

        System.out.println(X+Y);
        System.out.println(X-Y);
        System.out.println(X*Y);
        System.out.println(X/Y);

    }
}
